// Shuffle questions for each test session
export function getRandomQuestions(count = 20): PDRQuestion[] {
  const shuffled = [...PDR_QUESTIONS].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(count, shuffled.length));
}

export function getCategoryQuestions(category: string, count = 10): PDRQuestion[] {
  const filtered = PDR_QUESTIONS.filter((q) => q.category === category);
  const shuffled = filtered.sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}
